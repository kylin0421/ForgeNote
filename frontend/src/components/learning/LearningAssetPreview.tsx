function normalizeMindMapText(rawText: string) {
  const withoutClass = rawText
    .replace(/:::[A-Za-z0-9_-]+/g, '')
    .replace(/%%.*$/g, '')
    .trim()
  const withoutIcon = withoutClass.replace(/::icon\([^)]+\)/g, '').trim()
  const wrapped = [
    withoutIcon.match(/^[A-Za-z0-9_:-]+\(\((.*)\)\)$/),
    withoutIcon.match(/^[A-Za-z0-9_:-]+\((.*)\)$/),
    withoutIcon.match(/^[A-Za-z0-9_:-]+\[\[(.*)\]\]$/),
    withoutIcon.match(/^[A-Za-z0-9_:-]+\[(.*)\]$/),
    withoutIcon.match(/^[A-Za-z0-9_:-]+\{\{(.*)\}\}$/),
    withoutIcon.match(/^[A-Za-z0-9_:-]+\{(.*)\}$/),
  ].find(Boolean)

  return (wrapped?.[1] ?? withoutIcon)
    .replace(/^["']|["']$/g, '')
    .replace(/<br\s*\/?>/gi, ' ')
    .replace(/\s+/g, ' ')
    .trim()
}

