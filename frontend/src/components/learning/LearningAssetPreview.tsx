function QuizAsset({
  resource,
  compact = false,
  onLearningEvent,
}: {
  resource: LearningResource
  compact?: boolean
  onLearningEvent?: (event: LearningAssetInteractionEvent) => void
}) {
  const [answers, setAnswers] = useState<Record<string, number>>({})
  const [currentIndex, setCurrentIndex] = useState(0)
  const questions = getQuizQuestions(resource)
  const currentQuestion = questions[Math.min(currentIndex, questions.length - 1)]
  const answeredCount = Object.keys(answers).length
  const score = questions.reduce((total, question) => {
    return total + (answers[question.id] === question.answer_index ? 1 : 0)
  }, 0)

  if (questions.length === 0 || !currentQuestion) {
    return <MarkdownLikeAsset content={resource.content} compact={compact} compactHeight="max-h-80" />
  }

  const handleAnswer = (
    question: QuizQuestion,
    questionIndex: number,
    optionIndex: number
  ) => {
    const nextAnswers = {
      ...answers,
      [question.id]: optionIndex,
    }
    setAnswers(nextAnswers)

    const nextScore = questions.reduce((total, item) => {
      return total + (nextAnswers[item.id] === item.answer_index ? 1 : 0)
    }, 0)
    const correct = optionIndex === question.answer_index
    onLearningEvent?.({
      eventType: 'quiz_answer',
      summary: `Quiz "${resource.title}" question ${questionIndex + 1}/${questions.length} ${
        correct ? 'correct' : 'incorrect'
      } score ${nextScore}/${questions.length}. Prompt: ${question.prompt}`,
    })
  }

  const selected = answers[currentQuestion.id]
  const answered = selected !== undefined

  return (
    <div
      className={cn(
        'space-y-3',
        compact ? 'max-h-80 overflow-y-auto pr-1' : 'max-h-[calc(90vh-220px)] overflow-y-auto pr-1'
      )}
    >
      <div className="flex items-center justify-between rounded-md border bg-muted/40 px-3 py-2 text-sm">
        <span>{currentIndex + 1}/{questions.length}</span>
        <span className="font-medium">Score {score}/{questions.length}</span>
      </div>
      <div className="rounded-lg border p-3">
        <p className="text-sm font-medium">
          {currentIndex + 1}. {currentQuestion.prompt}
        </p>
        <div className="mt-3 grid gap-2">
          {currentQuestion.options.map((option, optionIndex) => {
            const isSelected = selected === optionIndex
            const isCorrect = currentQuestion.answer_index === optionIndex
            return (
              <button
                key={`${currentQuestion.id}-${optionIndex}`}
                type="button"
                onClick={() => handleAnswer(currentQuestion, currentIndex, optionIndex)}
                className={cn(
                  'rounded-md border px-3 py-2 text-left text-sm transition-colors',
                  answered &&
                    isCorrect &&
                    'border-emerald-500 bg-emerald-50 text-emerald-800 dark:bg-emerald-950 dark:text-emerald-200',
                  answered &&
                    isSelected &&
                    !isCorrect &&
                    'border-destructive bg-destructive/10 text-destructive',
                  !answered && 'hover:bg-muted'
                )}
              >
                {option}
              </button>
            )
          })}
        </div>
        {answered && (
          <p className="mt-3 rounded-md bg-muted px-3 py-2 text-sm">
            {currentQuestion.explanation}
          </p>
        )}
      </div>
      <div className="flex items-center justify-between gap-2">
        <span className="text-xs text-muted-foreground">
          Answered {answeredCount}/{questions.length}
        </span>
        <div className="flex gap-2">
          <Button
            type="button"
            variant="outline"
            size="sm"
            disabled={currentIndex === 0}
            onClick={() => setCurrentIndex((index) => Math.max(0, index - 1))}
          >
            Prev
          </Button>
          <Button
            type="button"
            variant="outline"
            size="sm"
            disabled={currentIndex === questions.length - 1}
            onClick={() => setCurrentIndex((index) => Math.min(questions.length - 1, index + 1))}
          >
            Next
          </Button>
        </div>
      </div>
    </div>
  )
}

function FlashcardAsset({ resource, compact = false }: { resource: LearningResource; compact?: boolean }) {
  const [flipped, setFlipped] = useState<Record<number, boolean>>({})
  const [currentIndex, setCurrentIndex] = useState(0)
  const cards = getFlashcards(resource)
  const currentCard = cards[Math.min(currentIndex, cards.length - 1)]

  if (cards.length === 0 || !currentCard) {
    return <MarkdownLikeAsset content={resource.content} compact={compact} compactHeight="max-h-80" />
  }

  const isFlipped = flipped[currentIndex]

  return (
    <div
      className={cn(
        'space-y-3',
        compact ? 'max-h-80 overflow-y-auto pr-1' : 'max-h-[calc(90vh-220px)] overflow-y-auto pr-1'
      )}
    >
      <div className="rounded-lg border p-4">
        <p className="text-xs font-medium text-muted-foreground">
          Card {currentIndex + 1}/{cards.length}
        </p>
        <p className="mt-3 min-h-24 text-sm leading-6">
          {isFlipped ? currentCard.back : currentCard.front}
        </p>
        {!isFlipped && currentCard.hint && (
          <p className="mt-2 rounded-md bg-muted px-3 py-2 text-xs text-muted-foreground">
            Hint: {currentCard.hint}
          </p>
        )}
        <Button
          type="button"
          variant="outline"
          size="sm"
          className="mt-3"
          onClick={() =>
            setFlipped((previous) => ({
              ...previous,
              [currentIndex]: !previous[currentIndex],
            }))
          }
        >
          {isFlipped ? 'Back' : 'Answer'}
        </Button>
      </div>
      <div className="flex justify-end gap-2">
        <Button
          type="button"
          variant="outline"
          size="sm"
          disabled={currentIndex === 0}
          onClick={() => setCurrentIndex((index) => Math.max(0, index - 1))}
        >
          Prev
        </Button>
        <Button
          type="button"
          variant="outline"
          size="sm"
          disabled={currentIndex === cards.length - 1}
          onClick={() => setCurrentIndex((index) => Math.min(cards.length - 1, index + 1))}
        >
          Next
        </Button>
      </div>
    </div>
  )
}

function normalizeMindMapText(rawText: string) {
  const withoutClass = rawText.replace(/:::[A-Za-z0-9_-]+/g, '').trim()
  const withoutIcon = withoutClass.replace(/::icon\([^)]+\)/g, '').trim()
  const wrapped = [
    withoutIcon.match(/^[A-Za-z0-9_-]+\(\((.*)\)\)$/),
    withoutIcon.match(/^[A-Za-z0-9_-]+\((.*)\)$/),
    withoutIcon.match(/^[A-Za-z0-9_-]+\[(.*)\]$/),
    withoutIcon.match(/^[A-Za-z0-9_-]+\{\{(.*)\}\}$/),
  ].find(Boolean)

  return (wrapped?.[1] ?? withoutIcon).trim()
}

function parseMindMapNodes(content: string) {
  const normalizedContent = content
    .trim()
    .replace(/^```(?:mermaid|mindmap)?\s*\n([\s\S]*?)\n```$/i, '$1')
    .trim()
  const lines = normalizedContent.split('\n')
  const roots: MindMapNode[] = []
  const stack: Array<{ level: number; node: MindMapNode }> = []
  let inCodeFence = false
  let inMermaidMindMap = false
  let cursor = 0

  for (const line of lines) {
    const trimmed = line.trim()
    if (!trimmed) {
      continue
    }

    if (trimmed.startsWith('```')) {
      inCodeFence = !inCodeFence
      continue
    }
    if (inCodeFence) {
      continue
    }

    if (/^mindmap(?:\s|$)/i.test(trimmed)) {
      inMermaidMindMap = true
      continue
    }

    const headingMatch = line.match(/^(\s*)(#{1,6})\s+(.*)$/)
    const listMatch = line.match(/^([ \t]*)(?:[-*+]|\d+\.)\s+(.*)$/)
    const mermaidMatch = inMermaidMindMap
      ? line.match(/^([ \t]*)(?!%%)(?!classDef\b)(?!class\b)(?!style\b)(.+)$/)
      : null

    if (!headingMatch && !listMatch && !mermaidMatch) {
      continue
    }

    const rawText =
      headingMatch?.[3]?.trim() ??
      listMatch?.[2]?.trim() ??
      mermaidMatch?.[2]?.trim() ??
      ''
    const text = normalizeMindMapText(rawText)
    if (!text) {
      continue
    }

    const indentLevel = headingMatch
      ? Math.max(0, headingMatch[2].length - 1)
      : Math.max(
          0,
          Math.floor(
            (listMatch?.[1] ?? mermaidMatch?.[1] ?? '').replace(/\t/g, '    ').length / 2
          ) + (mermaidMatch ? 0 : 1)
        )
    const level = indentLevel

    const node: MindMapNode = {
      id: `${text}-${cursor}-${level}-${roots.length}`,
      text,
      level,
      children: [],
    }
    cursor += 1

    while (stack.length > 0 && stack[stack.length - 1].level >= level) {
      stack.pop()
    }

    if (stack.length > 0) {
      stack[stack.length - 1].node.children.push(node)
    } else {
      roots.push(node)
    }

    stack.push({ level, node })
  }

  return roots
}

function isProfileArtifactMindMapContent(content: string) {
  const compact = content.toLowerCase()
  return (
    compact.includes('learning_profile') ||
    /(^|\n)\s*-\s*id:\s*source:/i.test(content) ||
    content.includes('学习画像')
  )
}

function MindMapNodeView({ node, depth = 0 }: { node: MindMapNode; depth?: number }) {
  return (
    <div className="flex min-w-0 flex-col items-center">
      <div
        className={cn(
          'min-w-32 max-w-56 rounded-md border px-3 py-2 text-center shadow-sm',
          depth === 0
            ? 'border-primary/40 bg-primary text-primary-foreground'
            : depth === 1
              ? 'bg-muted'
              : 'bg-background'
        )}
      >
        <p className={cn('break-words text-sm leading-5', depth === 0 && 'font-semibold')}>
          {node.text}
        </p>
      </div>
      {node.children.length > 0 && (
        <>
          <div className="h-5 w-px bg-border" />
          <div
            className={cn(
              'grid w-full gap-3',
              node.children.length === 1
                ? 'grid-cols-1 justify-items-center'
                : 'grid-cols-[repeat(auto-fit,minmax(8.5rem,1fr))]'
            )}
          >
          {node.children.map((child) => (
            <div key={child.id} className="relative flex justify-center">
              <span className="absolute -top-3 left-1/2 h-3 w-px -translate-x-1/2 bg-border" />
              <MindMapNodeView node={child} depth={depth + 1} />
            </div>
          ))}
          </div>
        </>
      )}
    </div>
  )
}

function MindMapAsset({ content, compact = false }: { content: string; compact?: boolean }) {
  const nodes = useMemo(() => parseMindMapNodes(content), [content])

  if (nodes.length === 0 || isProfileArtifactMindMapContent(content)) {
    return (
      <MarkdownLikeAsset
        content={
          isProfileArtifactMindMapContent(content)
            ? '这条思维导图内容来自学习画像元数据，不适合作为知识导图渲染。请重新生成该资产。'
            : content
        }
        compact={compact}
        compactHeight="max-h-80"
      />
    )
  }

  return (
    <article
      className={cn(
        'overflow-auto rounded-md border bg-background p-4',
        compact ? 'max-h-80' : 'max-h-[540px]'
      )}
    >
      <div className={cn('min-w-[560px] space-y-6', compact && 'min-w-[460px]')}>
        {nodes.map((node) => (
          <MindMapNodeView key={node.id} node={node} />
        ))}
      </div>
    </article>
  )
}

export function LearningAssetPreview({
  resource,
  compact = false,
  onLearningEvent,
}: {
  resource: LearningResource
  compact?: boolean
  onLearningEvent?: (event: LearningAssetInteractionEvent) => void
}) {
  if (resource.kind === 'quiz') {
    return <QuizAsset resource={resource} compact={compact} onLearningEvent={onLearningEvent} />
  }
  if (resource.kind === 'flashcards') {
    return <FlashcardAsset resource={resource} compact={compact} />
  }
  if (resource.kind === 'mind_map') {
    return <MindMapAsset content={resource.content} compact={compact} />
  }
  return (
    <MarkdownLikeAsset
      content={resource.content}
      compact={compact}
      compactHeight={resource.kind === 'code_lab' ? 'max-h-52' : 'max-h-[540px]'}
    />
  )
}
