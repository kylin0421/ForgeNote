export function getLearningAssetKindLabel(
  kind?: string | null,
  t?: TFunction<'translation', undefined>
) {
  if (!kind) {
    return 'Asset'
  }
  if (t) {
    const key = `learningAssets.kinds.${kind}`
    const translated = t(key)
    if (translated !== key) {
      return translated
    }
  }
  return LEARNING_ASSET_KIND_LABELS[kind] || kind
}

export function getLearningAssetTypeLabel(
  resource?: Pick<LearningResource, 'kind'> | null,
  t?: TFunction<'translation', undefined>
) {
  return getLearningAssetKindLabel(resource?.kind, t)
}

export function serializeLearningAssetNote(
  resource: LearningResource,
  kindLabel = getLearningAssetKindLabel(resource.kind)
) {
  const metadata = JSON.stringify({ ...resource, type: kindLabel })
  const extraSections: string[] = []
  const questions = getQuizQuestions(resource)
  const cards = getFlashcards(resource)

  if (resource.kind === 'quiz' && questions.length > 0) {
    extraSections.push(
      '## Quiz summary',
      questions
        .map(
          (question, index) =>
            `${index + 1}. ${question.prompt}\nAnswer: ${question.options[question.answer_index]}\nExplanation: ${question.explanation}`
        )
        .join('\n\n')
    )
  }

  if (resource.kind === 'flashcards' && cards.length > 0) {
    extraSections.push(
      '## Flashcards',
      cards
        .map(
          (card, index) =>
            `Card ${index + 1}\nFront: ${card.front}\nBack: ${card.back}`
        )
        .join('\n\n')
    )
  }

  const visibleContent = [
    `# ${resource.title}`,
    `Asset kind: ${kindLabel}`,
    `Type: ${kindLabel}`,
    `Format: ${resource.format}`,
    `Generated by: ${resource.agent}`,
    '',
    '## Objective',
    resource.summary,
    '',
    '## Content',
    resource.content,
    '',
    ...extraSections,
    '',
    `Tags: ${resource.tags.join(', ')}`,
  ].join('\n')

  return [ASSET_METADATA_START, metadata, ASSET_METADATA_END, '', visibleContent].join('\n')
}

export function parseLearningAssetNote(content?: string | null) {
  if (!content) {
    return null
  }

  const start = content.indexOf(ASSET_METADATA_START)
  if (start === -1) {
    return null
  }

  const metadataStart = start + ASSET_METADATA_START.length
  const end = content.indexOf(ASSET_METADATA_END, metadataStart)
  if (end === -1) {
    return null
  }

  const rawMetadata = content.slice(metadataStart, end).trim()
  try {
    return JSON.parse(rawMetadata) as LearningResource
  } catch {
    return null
  }
}

export function getVisibleLearningAssetContent(content?: string | null) {
  if (!content) {
    return ''
  }

  const start = content.indexOf(ASSET_METADATA_START)
  if (start === -1) {
    return content
  }

  const end = content.indexOf(ASSET_METADATA_END, start + ASSET_METADATA_START.length)
  if (end === -1) {
    return content
  }

  return content.slice(end + ASSET_METADATA_END.length).trim()
}

