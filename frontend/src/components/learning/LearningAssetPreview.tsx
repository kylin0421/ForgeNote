function normalizeTableLine(line: string) {
  if (!/[|｜│]/.test(line)) {
    return line
  }

  let normalized = line.replace(/[｜│]/g, '|')
  if (/^\s*\|?[\s|:—–－─-]+\|?\s*$/.test(normalized)) {
    normalized = normalized.replace(/[—–－─]/g, '-')
  }
  return normalized
}

function tableCells(line: string) {
  const normalized = normalizeTableLine(line).trim()
  if (!normalized.includes('|')) {
    return []
  }
  return normalized.replace(/^\|/, '').replace(/\|$/, '').split('|')
}

function isTableCandidateLine(line: string) {
  return tableCells(line).length >= 2
}

function isTableDelimiterLine(line: string) {
  const cells = tableCells(line)
  return cells.length >= 2 && cells.every((cell) => /^:?-{3,}:?$/.test(cell.trim()))
}

function tableDelimiterFor(line: string) {
  return `| ${tableCells(line).map(() => '---').join(' | ')} |`
}

function repairMarkdownTables(content: string) {
  const repaired = content
    .replace(/([*_])\n\1([ \t]*[|｜│])/g, '$1$1$2')

  const lines = repaired.split('\n')
  const output: string[] = []
  let inFence = false
  let inTable = false

  for (let index = 0; index < lines.length; index += 1) {
    const rawLine = lines[index]
    if (/^\s*```/.test(rawLine)) {
      inFence = !inFence
      inTable = false
      output.push(rawLine)
      continue
    }

    if (inFence) {
      output.push(rawLine)
      continue
    }

    if (!rawLine.trim()) {
      const nextLine = lines[index + 1] ? normalizeTableLine(lines[index + 1]) : ''
      if (inTable && isTableCandidateLine(nextLine)) {
        continue
      }
      inTable = false
      output.push(rawLine)
      continue
    }

    const line = normalizeTableLine(rawLine)
    const isTableLine = isTableCandidateLine(line)
    if (!isTableLine) {
      inTable = false
      output.push(line)
      continue
    }

    const nextLine = lines[index + 1] ? normalizeTableLine(lines[index + 1]) : ''
    output.push(line)
    if (
      !inTable &&
      isTableCandidateLine(nextLine) &&
      !isTableDelimiterLine(nextLine)
    ) {
      output.push(tableDelimiterFor(line))
    }
    inTable = true
  }

  return output.join('\n')
}

function splitRunOnMarkdownBlocks(content: string) {
  const lines = content.split('\n')
  let inFence = false

  return lines
    .map((line) => {
      if (/^\s*```/.test(line)) {
        inFence = !inFence
        return line
      }
      if (inFence || isTableCandidateLine(line)) {
        return line
      }
      return line
        .replace(/([^\n])([ \t]+)(#{1,6}\s+)/g, '$1\n\n$3')
        .replace(/([^\n])([ \t]+)([-*+]\s+)/g, '$1\n$3')
        .replace(/([^\n])([ \t]+)(\d+\.\s+)/g, '$1\n$3')
    })
    .join('\n')
}

export function normalizeMarkdownContent(content: string) {
  const unwrapMarkdownFence = (value: string) => {
    const fencedMarkdown = value.match(/^```(?:markdown|md|mdx)?\s*\n([\s\S]*?)\n```$/i)
    return fencedMarkdown?.[1] ? fencedMarkdown[1].trim() : value
  }

  const decodeJsonString = (value: string) => {
    const trimmed = value.trim()
    if (!/^["']/.test(trimmed)) {
      return trimmed
    }
    try {
      const decoded = JSON.parse(trimmed)
      return typeof decoded === 'string' ? decoded.trim() : trimmed
    } catch {
      return trimmed
    }
  }

  let normalized = unwrapMarkdownFence(decodeJsonString(content))
  const escapedNewlineCount = (normalized.match(/\\n/g) || []).length
  if (escapedNewlineCount > 1 || (!normalized.includes('\n') && escapedNewlineCount > 0)) {
    normalized = normalized
      .replace(/\\r\\n/g, '\n')
      .replace(/\\n/g, '\n')
      .replace(/\\t/g, '  ')
  }

  normalized = repairMarkdownTables(unwrapMarkdownFence(normalized).replace(/[ \t]+$/gm, ''))
  normalized = splitRunOnMarkdownBlocks(normalized)

  return repairMarkdownTables(normalized)
    .replace(/(^|\n)[ \t]*#{1,6}[ \t]*(?=\n)/g, '\n')
    .replace(/\n{3,}/g, '\n\n')
    .trim()
}

