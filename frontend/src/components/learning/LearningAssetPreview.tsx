function tokenizeCodeLine(line: string): Token[] {
  const regex =
    /(\/\*[\s\S]*?\*\/|\/\/.*$)|("(?:[^"\\]|\\.)*"|'(?:[^'\\]|\\.)*'|`(?:[^`\\]|\\.)*`)|\b(?:const|let|var|function|return|if|else|for|while|switch|case|break|continue|try|catch|finally|throw|new|class|import|from|export|default|await|async|this|super|extends|typeof|instanceof|true|false|null|undefined|NaN|Infinity|def|yield|interface|type|enum|readonly|public|private|protected|namespace|implements)\b|\b\d+(?:\.\d+)?\b|[{}[\]().,;+*\/=<>:!?\-]/g
  const tokens: Token[] = []
  let position = 0

  for (;;) {
    const next = regex.exec(line)
    if (!next) {
      if (position < line.length) {
        tokens.push({ text: line.slice(position) })
      }
      break
    }

    if (next.index > position) {
      tokens.push({ text: line.slice(position, next.index) })
    }

    const matchText = next[0]
    let className = ''
    if (next[1]) {
      className = 'text-emerald-600 dark:text-emerald-400'
    } else if (next[2]) {
      className = 'text-amber-700 dark:text-amber-300'
    } else if (next[3]) {
      className = 'text-blue-700 dark:text-blue-300'
    } else if (next[4]) {
      className = 'text-violet-600 dark:text-violet-300'
    } else if (next[5]) {
      className = 'text-rose-600 dark:text-rose-300'
    }

    tokens.push({
      text: matchText,
      className: className || undefined,
    })
    position = next.index + matchText.length
  }

  return tokens
}

