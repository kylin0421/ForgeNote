const LEARNING_ASSET_KIND_LABELS: Record<string, string> = {
  study_guide: 'Study guide',
  quiz: 'Quiz',
  flashcards: 'Flashcards',
  mind_map: 'Mind map',
  reading: 'Reading',
  code_lab: 'Code lab',
}

export function getLearningAssetKindLabel(
  kind?: string | null,
  t?: TFunction<'translation', undefined>
) {
  if (!kind) {
    return 'Asset'
  }
  if (t) {
    const key = `learningAssets.kinds.${kind}`
    const translated = t(key)
    if (translated !== key) {
      return translated
    }
  }
  return LEARNING_ASSET_KIND_LABELS[kind] || kind
}

export function getLearningAssetTypeLabel(
  resource?: Pick<LearningResource, 'kind'> | null,
  t?: TFunction<'translation', undefined>
) {
  return getLearningAssetKindLabel(resource?.kind, t)
}

export function serializeLearningAssetNote(
  resource: LearningResource,
  kindLabel = getLearningAssetKindLabel(resource.kind)
) {
  const metadata = JSON.stringify({ ...resource, type: kindLabel })
  const extraSections: string[] = []
  const questions = getQuizQuestions(resource)
  const cards = getFlashcards(resource)

  if (resource.kind === 'quiz' && questions.length > 0) {
    extraSections.push(
      '## Quiz summary',
      questions
        .map(
          (question, index) =>
            `${index + 1}. ${question.prompt}\nAnswer: ${question.options[question.answer_index]}\nExplanation: ${question.explanation}`
        )
        .join('\n\n')
    )
  }

  if (resource.kind === 'flashcards' && cards.length > 0) {
    extraSections.push(
      '## Flashcards',
      cards
        .map(
          (card, index) =>
            `Card ${index + 1}\nFront: ${card.front}\nBack: ${card.back}`
        )
        .join('\n\n')
    )
  }

  const visibleContent = [
    `# ${resource.title}`,
    `Asset kind: ${kindLabel}`,
    `Type: ${kindLabel}`,
    `Format: ${resource.format}`,
    `Generated by: ${resource.agent}`,
    '',
    '## Objective',
    resource.summary,
    '',
    '## Content',
    resource.content,
    '',
    ...extraSections,
    '',
    `Tags: ${resource.tags.join(', ')}`,
  ].join('\n')

  return [ASSET_METADATA_START, metadata, ASSET_METADATA_END, '', visibleContent].join('\n')
}

export function parseLearningAssetNote(content?: string | null) {
  if (!content) {
    return null
  }

  const start = content.indexOf(ASSET_METADATA_START)
  if (start === -1) {
    return null
  }

  const metadataStart = start + ASSET_METADATA_START.length
  const end = content.indexOf(ASSET_METADATA_END, metadataStart)
  if (end === -1) {
    return null
  }

  const rawMetadata = content.slice(metadataStart, end).trim()
  try {
    return JSON.parse(rawMetadata) as LearningResource
  } catch {
    return null
  }
}

export function getVisibleLearningAssetContent(content?: string | null) {
  if (!content) {
    return ''
  }

  const start = content.indexOf(ASSET_METADATA_START)
  if (start === -1) {
    return content
  }

  const end = content.indexOf(ASSET_METADATA_END, start + ASSET_METADATA_START.length)
  if (end === -1) {
    return content
  }

  return content.slice(end + ASSET_METADATA_END.length).trim()
}

function getQuizQuestions(resource: LearningResource): QuizQuestion[] {
  const questions = resource.payload?.questions
  return Array.isArray(questions) ? (questions as QuizQuestion[]) : []
}

function getFlashcards(resource: LearningResource): Flashcard[] {
  const cards = resource.payload?.cards
  return Array.isArray(cards) ? (cards as Flashcard[]) : []
}

function tokenizeCodeLine(line: string): Token[] {
  const regex =
    /(\/\*[\s\S]*?\*\/|\/\/.*$)|("(?:[^"\\]|\\.)*"|'(?:[^'\\]|\\.)*'|`(?:[^`\\]|\\.)*`)|\b(?:const|let|var|function|return|if|else|for|while|switch|case|break|continue|try|catch|finally|throw|new|class|import|from|export|default|await|async|this|super|extends|typeof|instanceof|true|false|null|undefined|NaN|Infinity|def|yield|interface|type|enum|readonly|public|private|protected|namespace|implements)\b|\b\d+(?:\.\d+)?\b|[{}[\]().,;+*\/=<>:!?\-]/g
  const tokens: Token[] = []
  let position = 0

  for (;;) {
    const next = regex.exec(line)
    if (!next) {
      if (position < line.length) {
        tokens.push({ text: line.slice(position) })
      }
      break
    }

    if (next.index > position) {
      tokens.push({ text: line.slice(position, next.index) })
    }

    const matchText = next[0]
    let className = ''
    if (next[1]) {
      className = 'text-emerald-600 dark:text-emerald-400'
    } else if (next[2]) {
      className = 'text-amber-700 dark:text-amber-300'
    } else if (next[3]) {
      className = 'text-blue-700 dark:text-blue-300'
    } else if (next[4]) {
      className = 'text-violet-600 dark:text-violet-300'
    } else if (next[5]) {
      className = 'text-rose-600 dark:text-rose-300'
    }

    tokens.push({
      text: matchText,
      className: className || undefined,
    })
    position = next.index + matchText.length
  }

  return tokens
}

function CodeHighlightedBlock({ code, language, compact, className }: {
  code: string
  language?: string
  compact: boolean
  className?: string
}) {
  const lines = useMemo(() => code.replace(/\n$/, '').split('\n'), [code])

  return (
    <div
      className={cn(
        'relative overflow-auto rounded-md bg-muted/80 p-3',
        compact ? 'max-h-80' : 'max-h-52'
      )}
    >
      <pre className={cn('min-w-0', className)}>
        <code className="font-mono text-xs leading-5">
          {lines.map((line, lineIndex) => (
            <span key={`${line}-${lineIndex}`} className="table-row">
              <span className="table-cell select-none opacity-70 pr-4 text-right">
                {lineIndex + 1}
              </span>
              <span className="table-cell whitespace-pre">
                {tokenizeCodeLine(line).map((token, tokenIndex) => (
                  <span
                    key={`${line}-${lineIndex}-${tokenIndex}`}
                    className={cn(
                      token.className,
                      language ? 'language-' + language : undefined
                    )}
                  >
                    {token.text}
                  </span>
                ))}
              </span>
            </span>
          ))}
        </code>
      </pre>
    </div>
  )
}

function normalizeTableLine(line: string) {
  if (!/[|｜│]/.test(line)) {
    return line
  }

  let normalized = line.replace(/[｜│]/g, '|')
  if (/^\s*\|?[\s|:—–－─-]+\|?\s*$/.test(normalized)) {
    normalized = normalized.replace(/[—–－─]/g, '-')
  }
  return normalized
}

function tableCells(line: string) {
  const normalized = normalizeTableLine(line).trim()
  if (!normalized.includes('|')) {
    return []
  }
  return normalized.replace(/^\|/, '').replace(/\|$/, '').split('|')
}

function isTableCandidateLine(line: string) {
  return tableCells(line).length >= 2
}

function isTableDelimiterLine(line: string) {
  const cells = tableCells(line)
  return cells.length >= 2 && cells.every((cell) => /^:?-{3,}:?$/.test(cell.trim()))
}

function tableDelimiterFor(line: string) {
  return `| ${tableCells(line).map(() => '---').join(' | ')} |`
}

function repairMarkdownTables(content: string) {
  const repaired = content
    .replace(/([*_])\n\1([ \t]*[|｜│])/g, '$1$1$2')

  const lines = repaired.split('\n')
  const output: string[] = []
  let inFence = false
  let inTable = false

  for (let index = 0; index < lines.length; index += 1) {
    const rawLine = lines[index]
    if (/^\s*```/.test(rawLine)) {
      inFence = !inFence
      inTable = false
      output.push(rawLine)
      continue
    }

    if (inFence) {
      output.push(rawLine)
      continue
    }

    if (!rawLine.trim()) {
      const nextLine = lines[index + 1] ? normalizeTableLine(lines[index + 1]) : ''
      if (inTable && isTableCandidateLine(nextLine)) {
        continue
      }
      inTable = false
      output.push(rawLine)
      continue
    }

    const line = normalizeTableLine(rawLine)
    const isTableLine = isTableCandidateLine(line)
    if (!isTableLine) {
      inTable = false
      output.push(line)
      continue
    }

    const nextLine = lines[index + 1] ? normalizeTableLine(lines[index + 1]) : ''
    output.push(line)
    if (
      !inTable &&
      isTableCandidateLine(nextLine) &&
      !isTableDelimiterLine(nextLine)
    ) {
      output.push(tableDelimiterFor(line))
    }
    inTable = true
  }

  return output.join('\n')
}

function splitRunOnMarkdownBlocks(content: string) {
  const lines = content.split('\n')
  let inFence = false

  return lines
    .map((line) => {
      if (/^\s*```/.test(line)) {
        inFence = !inFence
        return line
      }
      if (inFence || isTableCandidateLine(line)) {
        return line
      }
      return line
        .replace(/([^\n])([ \t]+)(#{1,6}\s+)/g, '$1\n\n$3')
        .replace(/([^\n])([ \t]+)([-*+]\s+)/g, '$1\n$3')
        .replace(/([^\n])([ \t]+)(\d+\.\s+)/g, '$1\n$3')
    })
    .join('\n')
}

export function normalizeMarkdownContent(content: string) {
  const unwrapMarkdownFence = (value: string) => {
    const fencedMarkdown = value.match(/^```(?:markdown|md|mdx)?\s*\n([\s\S]*?)\n```$/i)
    return fencedMarkdown?.[1] ? fencedMarkdown[1].trim() : value
  }

  const decodeJsonString = (value: string) => {
    const trimmed = value.trim()
    if (!/^["']/.test(trimmed)) {
      return trimmed
    }
    try {
      const decoded = JSON.parse(trimmed)
      return typeof decoded === 'string' ? decoded.trim() : trimmed
    } catch {
      return trimmed
    }
  }

  let normalized = unwrapMarkdownFence(decodeJsonString(content))
  const escapedNewlineCount = (normalized.match(/\\n/g) || []).length
  if (escapedNewlineCount > 1 || (!normalized.includes('\n') && escapedNewlineCount > 0)) {
    normalized = normalized
      .replace(/\\r\\n/g, '\n')
      .replace(/\\n/g, '\n')
      .replace(/\\t/g, '  ')
  }

  normalized = repairMarkdownTables(unwrapMarkdownFence(normalized).replace(/[ \t]+$/gm, ''))
  normalized = splitRunOnMarkdownBlocks(normalized)

  return repairMarkdownTables(normalized)
    .replace(/(^|\n)[ \t]*#{1,6}[ \t]*(?=\n)/g, '\n')
    .replace(/\n{3,}/g, '\n\n')
    .trim()
}

